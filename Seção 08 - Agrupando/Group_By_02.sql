/* Selecionando Dados da Tabela */
SELECT
    BAIRRO
  , COUNT(SEXO)			AS [CONTAGEM DE SEXO]
FROM
	[SQL_DE_CADA_DIA].[DBO].[ALUNO]
GROUP BY
	BAIRRO
GO


SELECT
    BAIRRO
  , COUNT(SEXO)		AS [CONTAGEM DE SEXO]
FROM
	[SQL_DE_CADA_DIA].[DBO].[ALUNO]
WHERE
	BAIRRO IS NOT NULL
GROUP BY
	BAIRRO
GO


SELECT
    CIDADE
  , BAIRRO
  , COUNT(*)		AS [CONTAGEM DE LINHAS]
FROM
	[SQL_DE_CADA_DIA].[DBO].[ALUNO]
WHERE
	CIDADE IS NOT NULL
GROUP BY
    CIDADE
  , BAIRRO
ORDER BY
	BAIRRO DESC
GO
